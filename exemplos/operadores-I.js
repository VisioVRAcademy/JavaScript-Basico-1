// OPERADORES I ---------------------------------------------------------------

// Exemplo 1 - Operadores matemáticos
console.log(10 + 20); // Soma
console.log(10 - 20); // Subtração
console.log(10 * 20); // Multiplicação
console.log(10 / 20); // Divisão

// Exemplo 2 - Operadores de comparação
console.log(5 > 3); // true
console.log(5 == 3); // false
console.log(5 == 5); // true

// Exemplo 3 - Operadores lógicos
console.log(true && false); // false
console.log(true || false); // true
