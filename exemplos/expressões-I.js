// EXPRESSÕES -----------------------------------------------------------------

// Exemplo 1 - Número
5; // É uma expressão. O resultado dela é o número 5.

// Exemplo 2 - Soma
2 + 3; // É uma expressão. O resultado é 5.

// Exemplo 3 - String
"Olá, mundo!"; // É uma expressão. O resultado é a string "Olá, mundo!".

// Exemplo 4 - Variável
var nome = "João";
nome; // Aqui, a expressão é a variável "nome", e o resultado é o valor que ela contém, ou seja, "João".

// Exemplo 5 - Comparação lógica
true && false; // Expressão lógica. O resultado é false.
