// DEFINIÇÃO DE VARIÁVEIS -----------------------------------------------------

// Exemplo 1 - Criação de variável
var idade = 25;
console.log(idade); // Isso vai mostrar o número 25 na tela

// Exemplo 2 - Atualização de variável
idade = 26;
console.log(idade); // Agora vai mostrar 26

// Exemplo 3 - Armazenamento de operações
var numero1 = 10;
var numero2 = 5;
var soma = numero1 + numero2;
console.log(soma); // Mostra 15, o resultado da soma
