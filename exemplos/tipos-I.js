// Exemplo 2 - Números (inteiros, decimais etc.)
var idade = 25;
console.log(idade); // Saída: 25

// Exemplo 3 - Booleanos
var estaChovendo = false;
console.log(estaChovendo); // Saída: false

// Exemplo 4 - Arrays (ou vetores/listas)
var numeros = [1, 2, 3];
console.log(numeros[0]); // 1
console.log(numeros[1]); // 2
console.log(numeros[2]); // 3

// Exemplo 5 - Objetos
// VER ARQUIVO: objetos.js

// Exemplo 6 - null
var ferramenta = null; // A variável ferramenta não tem valor
console.log(ferramenta); // null

// Exemplo 7 - undefined
var caixa; // A variável caixa foi criada, mas não tem nenhum valor armazenado nela
console.log(caixa); // undefined

// Exemplo 8 - NaN
var resultado = 0 / 0;
console.log(resultado); // NaN
